def disp_to_depth(disp, min_depth, max_depth):
    """Convert network's sigmoid output into depth prediction
    The formula for this conversion is given in the 'additional considerations'
    section of the paper.
    """
    min_disp = 1 / max_depth
    max_disp = 1 / min_depth
    scaled_disp = min_disp + (max_disp - min_disp) * disp
    depth = 1 / scaled_disp
    return scaled_disp, depth


def transformation_from_parameters(axisangle, translation, invert=False):
    """Convert the network's (axisangle, translation) output into a 4x4 matrix
    """
    R = rot_from_axisangle(axisangle)
    t = translation.clone()

    if invert:
        R = R.transpose(1, 2)
        t *= -1

    T = get_translation_matrix(t)

    if invert:
        M = torch.matmul(R, T)
    else:
        M = torch.matmul(T, R)

    return M


def get_translation_matrix(translation_vector):
    """Convert a translation vector into a 4x4 transformation matrix
    """
    T = torch.zeros(translation_vector.shape[0], 4, 4).to(device=translation_vector.device)

    t = translation_vector.contiguous().view(-1, 3, 1)

    T[:, 0, 0] = 1
    T[:, 1, 1] = 1
    T[:, 2, 2] = 1
    T[:, 3, 3] = 1
    T[:, :3, 3, None] = t

    return T


def rot_from_axisangle(vec):
    """Convert an axisangle rotation into a 4x4 transformation matrix
    (adapted from https://github.com/Wallacoloo/printipi)
    Input 'vec' has to be Bx1x3
    """
    angle = torch.norm(vec, 2, 2, True)
    axis = vec / (angle + 1e-7)

    ca = torch.cos(angle)
    sa = torch.sin(angle)
    C = 1 - ca

    x = axis[..., 0].unsqueeze(1)
    y = axis[..., 1].unsqueeze(1)
    z = axis[..., 2].unsqueeze(1)

    xs = x * sa
    ys = y * sa
    zs = z * sa
    xC = x * C
    yC = y * C
    zC = z * C
    xyC = x * yC
    yzC = y * zC
    zxC = z * xC

    rot = torch.zeros((vec.shape[0], 4, 4)).to(device=vec.device)

    rot[:, 0, 0] = torch.squeeze(x * xC + ca)
    rot[:, 0, 1] = torch.squeeze(xyC - zs)
    rot[:, 0, 2] = torch.squeeze(zxC + ys)
    rot[:, 1, 0] = torch.squeeze(xyC + zs)
    rot[:, 1, 1] = torch.squeeze(y * yC + ca)
    rot[:, 1, 2] = torch.squeeze(yzC - xs)
    rot[:, 2, 0] = torch.squeeze(zxC - ys)
    rot[:, 2, 1] = torch.squeeze(yzC + xs)
    rot[:, 2, 2] = torch.squeeze(z * zC + ca)
    rot[:, 3, 3] = 1

    return rot


class ConvBlock(nn.Module):
    """Layer to perform a convolution followed by ELU
    """
    def __init__(self, in_channels, out_channels):
        super(ConvBlock, self).__init__()

        self.conv = Conv3x3(in_channels, out_channels)
        self.nonlin = nn.ELU(inplace=True)

    def forward(self, x):
        out = self.conv(x)
        out = self.nonlin(out)
        return out


class Conv3x3(nn.Module):
    """Layer to pad and convolve input
    """
    def __init__(self, in_channels, out_channels, use_refl=True):
        super(Conv3x3, self).__init__()

        if use_refl:
            self.pad = nn.ReflectionPad2d(1)
        else:
            self.pad = nn.ZeroPad2d(1)
        self.conv = nn.Conv2d(int(in_channels), int(out_channels), 3)

    def forward(self, x):
        out = self.pad(x)
        out = self.conv(out)
        return out


class BackprojectDepth(nn.Module):
    """Layer to transform a depth image into a point cloud
    """
    def __init__(self, batch_size, height, width):
        super(BackprojectDepth, self).__init__()

        self.batch_size = batch_size
        self.height = height
        self.width = width

        meshgrid = np.meshgrid(range(self.width), range(self.height), indexing='xy')
        self.id_coords = np.stack(meshgrid, axis=0).astype(np.float32)
        self.id_coords = nn.Parameter(torch.from_numpy(self.id_coords),
                                      requires_grad=False)

        self.ones = nn.Parameter(torch.ones(self.batch_size, 1, self.height * self.width),
                                 requires_grad=False)

        self.pix_coords = torch.unsqueeze(torch.stack(
            [self.id_coords[0].view(-1), self.id_coords[1].view(-1)], 0), 0)
        self.pix_coords = self.pix_coords.repeat(batch_size, 1, 1)
        self.pix_coords = nn.Parameter(torch.cat([self.pix_coords, self.ones], 1),
                                       requires_grad=False)

    def forward(self, depth, inv_K):
        cam_points = torch.matmul(inv_K[:, :3, :3], self.pix_coords)
        cam_points = depth.view(self.batch_size, 1, -1) * cam_points
        cam_points = torch.cat([cam_points, self.ones], 1)

        return cam_points


class Project3D(nn.Module):
    """Layer which projects 3D points into a camera with intrinsics K and at position T
    """
    def __init__(self, batch_size, height, width, eps=1e-7):
        super(Project3D, self).__init__()

        self.batch_size = batch_size
        self.height = height
        self.width = width
        self.eps = eps

    def forward(self, points, K, T):
        P = torch.matmul(K, T)[:, :3, :]

        cam_points = torch.matmul(P, points)

        pix_coords = cam_points[:, :2, :] / (cam_points[:, 2, :].unsqueeze(1) + self.eps)
        pix_coords = pix_coords.view(self.batch_size, 2, self.height, self.width)
        pix_coords = pix_coords.permute(0, 2, 3, 1)
        pix_coords[..., 0] /= self.width - 1
        pix_coords[..., 1] /= self.height - 1
        pix_coords = (pix_coords - 0.5) * 2
        return pix_coords


class WarpHomography(nn.Module):
    """Layer to warp bev from one timestep to another
    """
    def __init__(self, batch_size, bev_height, bev_width, bev_res):
        super(WarpHomography, self).__init__()

        self.batch_size = batch_size
        self.height = bev_height
        self.width = bev_width
        self.res = bev_res

        # Generate orthographic camera intrinsics
        Kb = np.eye(4)
        Kb[0, 0] = 1/self.res
        Kb[1, 1] = 1/self.res
        Kb[0, 3] = self.width/2
        Kb[1, 3] = self.height/2

        ###TODO: Fix these transformations. Right now they assume camera is parallel to floor.
        Tb = np.eye(4)
        Tb[:3, 3] = [0, 0, -(self.res*self.height + 0.1)/2]  # to move to center of grid of range 0.1 to bh*res along z.

        Rb = np.eye(4)
        Rb[:3, :3] = Rotation.from_rotvec([np.pi/2, 0, 0]).as_matrix()

        # Projection matrix
        Pb = (Kb @ Rb @ Tb).astype(np.float32)
        self.Pb = torch.unsqueeze(torch.from_numpy(Pb), 0).repeat(self.batch_size, 1, 1)
        self.Pb = nn.Parameter(self.Pb, requires_grad=False)

        # Backprojected coords
        inv_Pb = np.linalg.inv(Pb)
        tbx, tby = np.meshgrid(np.arange(self.width),np.arange(self.height), indexing='xy')
        tbz = np.ones_like(tbx)
        frame_coords = np.stack([tbx, tby, tbz, np.ones_like(tbx)]).reshape((4, -1))  # 4 x n_points
        bp_coords = (inv_Pb @ frame_coords).astype(np.float32)
        self.bp_coords = torch.unsqueeze(torch.from_numpy(bp_coords), 0).repeat(self.batch_size, 1, 1)
        self.bp_coords = nn.Parameter(self.bp_coords, requires_grad=False)

        
    def forward(self, T):
        points = torch.matmul(T, self.bp_coords)
        cam_points = torch.matmul(self.Pb, points)

        pix_coords = cam_points[:, :2, :]
        pix_coords = pix_coords.view(self.batch_size, 2, self.height, self.width)
        pix_coords = pix_coords.permute(0, 2, 3, 1)
        pix_coords[..., 0] /= self.width - 1
        pix_coords[..., 1] /= self.height - 1
        pix_coords = (pix_coords - 0.5) * 2
        return pix_coords


    def project(self, points, semantics):
        cam_points = torch.matmul(self.Pb, points)
        Xb = cam_points[:, :3, :]
        valid_idx = ((Xb[:, 1, ...] >= 0) & (Xb[:, 1, ...] < self.height) & 
                    (Xb[:, 0, ...] >= 0) & (Xb[:, 0, ...] < self.width) &
                    (Xb[:, 2, ...] >= 0))

        u = Xb[:, 0, ...].long()
        v = Xb[:, 1, ...].long()

        mask_idx = v * self.width + u
        
        # For coordinates outside the range, replace them with coordinates of bottom center of the grid
        u = torch.where(valid_idx, u, self.width//2)
        v = torch.where(valid_idx, v, self.height-1)
        grid_idx = v * self.width + u

        # For coordinates outside the range, set their semantic value to 0- unknown.
        # For floor keep it as 2, for obstacles set it to -1000. Used in filtering post accumulation.
        semantics = (semantics.view((self.batch_size, -1)) == 2) + 1
        grid_seg = torch.where(valid_idx, semantics, 0)
        grid_seg = torch.where(grid_seg != 1, grid_seg, -1000)

        # Generate (batch indices, grid indices) to set values for semantic labels in grid
        # Note duplicate elements will be accumulated (summed).
        batch_indices = torch.arange(0, self.batch_size, \
            dtype=torch.int64).view((-1, 1)).repeat((1, grid_idx.shape[1])).view(-1)
        grid_idx = grid_idx.view(-1)
        grid_seg = grid_seg.view(-1)

        grid = torch.zeros((self.batch_size, self.height, self.width), dtype=torch.int64, \
            device=semantics.device)
        grid = grid.view((self.batch_size, -1))
        grid.index_put_((batch_indices, grid_idx), grid_seg, True)

        mask = torch.zeros((self.batch_size, self.height, self.width), dtype=torch.int64, \
            device=semantics.device)
        mask = mask.view((self.batch_size, -1))
        mask_idx = mask_idx.view(-1)
        valid_idx = valid_idx.view(-1).long()
        mask.index_put_((batch_indices, mask_idx), valid_idx, True)

        # Replace all positive values as floor(2) and all negative values as obstacles(1)
        grid = torch.where(grid > 0, 2, grid)
        grid = torch.where(grid < 0, 1, grid)

        mask = torch.where(mask > 0, 1, mask)

        grid = grid.view((self.batch_size, self.height, self.width))        
        return grid


def upsample(x, scale=2, mode="nearest"):
    """Upsample input tensor by a factor of 2
    """
    return F.interpolate(x, scale_factor=scale, mode=mode)


def get_smooth_loss(disp, img):
    """Computes the smoothness loss for a disparity image
    The color image is used for edge-aware smoothness
    """
    grad_disp_x = torch.abs(disp[:, :, :, :-1] - disp[:, :, :, 1:])
    grad_disp_y = torch.abs(disp[:, :, :-1, :] - disp[:, :, 1:, :])

    grad_img_x = torch.mean(torch.abs(img[:, :, :, :-1] - img[:, :, :, 1:]), 1, keepdim=True)
    grad_img_y = torch.mean(torch.abs(img[:, :, :-1, :] - img[:, :, 1:, :]), 1, keepdim=True)

    grad_disp_x *= torch.exp(-grad_img_x)
    grad_disp_y *= torch.exp(-grad_img_y)

    return grad_disp_x.mean() + grad_disp_y.mean()


class SSIM(nn.Module):
    """Layer to compute the SSIM loss between a pair of images
    """
    def __init__(self):
        super(SSIM, self).__init__()
        self.mu_x_pool   = nn.AvgPool2d(3, 1)
        self.mu_y_pool   = nn.AvgPool2d(3, 1)
        self.sig_x_pool  = nn.AvgPool2d(3, 1)
        self.sig_y_pool  = nn.AvgPool2d(3, 1)
        self.sig_xy_pool = nn.AvgPool2d(3, 1)

        self.refl = nn.ReflectionPad2d(1)

        self.C1 = 0.01 ** 2
        self.C2 = 0.03 ** 2

    def forward(self, x, y):
        x = self.refl(x)
        y = self.refl(y)

        mu_x = self.mu_x_pool(x)
        mu_y = self.mu_y_pool(y)

        sigma_x  = self.sig_x_pool(x ** 2) - mu_x ** 2
        sigma_y  = self.sig_y_pool(y ** 2) - mu_y ** 2
        sigma_xy = self.sig_xy_pool(x * y) - mu_x * mu_y

        SSIM_n = (2 * mu_x * mu_y + self.C1) * (2 * sigma_xy + self.C2)
        SSIM_d = (mu_x ** 2 + mu_y ** 2 + self.C1) * (sigma_x + sigma_y + self.C2)

        return torch.clamp((1 - SSIM_n / SSIM_d) / 2, 0, 1)


